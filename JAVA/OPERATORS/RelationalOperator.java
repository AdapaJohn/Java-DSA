package OPERATORS;

public class RelationalOperator {
    public static void main(String[] args) {
        int a = 7, b = 11;
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
    }
}
